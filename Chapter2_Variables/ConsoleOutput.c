#include <stdio.h>

int main()
{
    int a = 65;
    unsigned int b = 1U;
    float c = 1.0f;
    double d = 2.5;

    printf("The value of variable a is: %d\n", a);
    printf("The value of variable b is: %u\n", b);
    printf("The value of variable c is: %f\n", c);
    printf("The value of variable d is: %f\n", d);


    return 0;
}
